// Variablen
let a = 7; // Wir setzen die Variable a und können sie an jedem Punkt so überschreiben wie es uns passt
let b = 8;
let c = 10;

// Berechne folgendes und gib das Eregebnis über console.log aus 

// a + b + c 

let result = a + b + c;
console.log("Das Ergebnis beträgt: " + result);

// (a + b) / c 

result = (a + b) / c
console.log("Das Ergebnis beträgt: " + result);

// c -a + b 

result = c - a + b;
console.log("Das Ergebnis beträgt: " + result);

// a + b + c * 3 

result = a + b + (c * 3);
console.log("Das Ergebnis beträgt: " + result);

// (a + b + c)/4 
result = (a + b + c) / 4;
console.log("Das Ergebnis beträgt: " + result);


// Ändere die Variablen auf folgende Werte 

